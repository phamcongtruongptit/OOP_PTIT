    private void btthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btthemActionPerformed
        // TODO add your handling code here:
        int n = listpv.size();
        if(n>0)
            phongvien.setSma(listpv.get(n-1).getMa()+1);
        jTextField1.setText(phongvien.getSma()+"");
        String ten , diachi , loai;
        try {
            ten = jTextField2.getText();
            diachi = jTextField3.getText();
            loai = jComboBox1.getSelectedItem().toString();
            phongvien pv = new phongvien(ten, diachi, loai);
            tmpv.addRow(pv.toObjects());
            listpv.add(pv);
        } catch (trongexception e) {
            JOptionPane.showMessageDialog(this, "khong duoc de trong");
            return;
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "phai nhap so");
            return;
        }
    }//GEN-LAST:event_btthemActionPerformed

    private void btxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btxoaActionPerformed
        // TODO add your handling code here:
        int r =jTable1.getSelectedRow();
        if(r>=0 && r<jTable1.getRowCount()){
            tmpv.removeRow(r);
            listpv.remove(r);
        }else{
            JOptionPane.showMessageDialog(this, "chon dich vu de xoa");
            return;
        }
    }//GEN-LAST:event_btxoaActionPerformed

