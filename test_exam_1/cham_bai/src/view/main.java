    private void loadpv(){
        File f = new File(fpv);
        if(f.exists()){
            listpv = iofile.doc(fpv);
        }else{
            listpv = new ArrayList<>();
        }
    }
     private void loadkb(){
        File f = new File(fkb);
        if(f.exists()){
            listkb = iofile.doc(fkb);
        }else{
            listkb = new ArrayList<>();
        }
    }
     private void showpv(List<phongvien> list){
         tmpv.setRowCount(0);
         for(phongvien i: list)
             tmpv.addRow(i.toObjects());
     }
    private void showkb(List<kieubaiviet> list){
         tmkb.setRowCount(0);
         for(kieubaiviet i: list)
             tmkb.addRow(i.toObjects());
     }
